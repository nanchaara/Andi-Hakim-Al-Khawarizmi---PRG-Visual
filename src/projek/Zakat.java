    private void Tampilkan_Data() {
        DefaultTableModel model = new DefaultTableModel ();
        model.addColumn ("No");
        model.addColumn ("Kode Keluarga");
        model.addColumn ("Kepala");
        model.addColumn ("RT");
        model.addColumn ("Jumlah Anggota");
        model.addColumn ("Anggota");
        model.addColumn ("Zakat Fitrah");
        model.addColumn ("Zakat Maal");
        
        try {
            
            int no = 1;
            
            String sql = "SELECT * FROM keluarga WHERE kode_keluarga LIKE '%"
                      + cari.getText () + "%'"
                      + "OR kepala LIKE '%" + cari.getText () + "%'";
            java.sql.Connection conn = (Connection) koneksiZakat.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while (res.next()) {
                model.addRow(new Object[]{no++, res.getString(1),
                     res.getString(2), res.getString(3),
                     res.getString(4), res.getString(5),
                     res.getString(6), res.getString(7)});
            }
            tabel.setModel (model);
            
         } catch (SQLException e) {
            System.out.println ("Error : " + e.getMessage());
         }
    }
