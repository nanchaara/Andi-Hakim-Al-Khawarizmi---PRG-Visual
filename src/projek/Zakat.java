    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String rt = null;
        if (rt5.isSelected ()) {
            rt = "5";
        } else if (rt6.isSelected ()) {
            rt = "6";
        } else if (rt7.isSelected ()) {
            rt = "7";
        }
        
        try {
            String sql = "INSERT INTO keluarga VALUES ('" + kode_keluarga.getText()
                    + "','" + kepala.getText()
                    + "','" + rt
                    + "','" + jumlah_anggota.getText()
                    + "','" + anggota.getText()
                    + "','" + zakat_fitrah.getText()
                    + "','" + zakat_maal.getText() + "')";
            java.sql.Connection conn = (Connection) koneksiZakat.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Proses Simpan Berhasil!");
            Tampilkan_Data();
            Kosongkan_Form();
            
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog (this, e.getMessage ());
        }
        Kosongkan_Form();
    }//GEN-LAST:event_btnTambahActionPerformed

