    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        int baris = tabel.rowAtPoint(evt.getPoint());
        String kode = tabel.getValueAt(baris, 1).toString();
        kode_keluarga.setText(kode);
        
        String nama = tabel.getValueAt(baris, 2).toString();
        kepala.setText(nama);
        
        if ("5".equals(tabel.getValueAt (baris, 3).toString())) {
           rt5.setSelected (true);
        } else if ("6".equals(tabel.getValueAt (baris, 3).toString())) {
           rt6.setSelected (true);
        } else {
           rt7.setSelected (true);
        }
        String jml = tabel.getValueAt(baris, 4).toString();
        jumlah_anggota.setText(jml);
        
        String ang = tabel.getValueAt(baris, 5).toString();
        anggota.setText(ang);
        
        String zktF = tabel.getValueAt(baris, 6).toString();
        zakat_fitrah.setText(zktF);
        
        String zktM = tabel.getValueAt (baris, 7).toString() ;
        zakat_maal.setText(zktM);
    }//GEN-LAST:event_tabelMouseClicked

